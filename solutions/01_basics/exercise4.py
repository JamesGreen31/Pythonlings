
"""
The objective of this module is to learn the basics of working with strings,
as well as printing to the console.

In this module, you will implement functions to:
- Capitalize a string
- Lowercase a string
- Print the string with an "!" mark
"""

def upper(s: str) -> str:
    return s.upper()

def lower(s: str) -> str:
    return s.lower()

def show(s: str) -> None:
    print(f"{s}!")

